#' Build x_mat from a clean formula, preserving I(), interactions, offsets, etc.
#'
#' Replaces the fragile all.vars() + reformulate() approach. Uses model.matrix()
#' on the clean formula and removes columns belonging to mc variables.
#' @param clean_formula the formula with mc() wrappers stripped (bare variable names remain)
#' @param mc_variables character vector of mc variable names to exclude
#' @param data the data frame
#' @return a list with x_mat (the non-mc part of the model matrix) and
#'   x_terms (the terms object, for use in predict)
#' @keywords internal
.build_x_mat <- function(clean_formula, mc_variables, data) {
  tt <- terms(clean_formula)
  # Drop response so this works with newdata that lacks the response variable
  tt <- delete.response(tt)
  mf <- model.frame(tt, data = data, na.action = na.pass)
  full_mm <- model.matrix(tt, mf)

  # Identify columns generated by mc variables (factor dummies)
  # model.matrix names factor columns as "varnamelevel", so we match on the

  # variable name prefix. We must be careful not to match variables that
  # merely start with the same prefix, so we use the assignment attribute.
  asgn <- attr(full_mm, "assign")
  term_labels <- attr(tt, "term.labels")

  # Find which term indices correspond to mc variables (exact match)
  mc_term_idx <- which(term_labels %in% mc_variables)

  # Columns to drop: those assigned to mc terms

  mc_cols <- which(asgn %in% mc_term_idx)

  if (length(mc_cols) > 0) {
    x_mat <- full_mm[, -mc_cols, drop = FALSE]
  } else {
    x_mat <- full_mm
  }

  list(x_mat = x_mat, x_terms = tt, x_model_frame = mf)
}

#' Build design matrix [dummies(z, K-1), x] in R (mirrors C++ build_design)
#' @keywords internal
.build_xi_hat <- function(z_hat, x_mat, K) {
  n <- length(z_hat)
  s <- K - 1L
  dummies <- matrix(0, n, s)
  for (k in seq_len(s)) {
    dummies[, k] <- as.numeric(z_hat == k)
  }
  cbind(dummies, x_mat)
}

#' Generate parameter names for the SIMEX model (single mc variable)
#' @keywords internal
.make_param_names <- function(z_levels, x_mat, K) {
  if (K == 2L) {
    dummy_names <- z_levels[2]
  } else {
    dummy_names <- z_levels[-1]
  }
  c(dummy_names, colnames(x_mat))
}

#' Build design matrix for multiple mc variables
#' [dummies_z1(K1-1) | dummies_z2(K2-1) | ... | x_mat]
#' @keywords internal
.build_multi_xi_hat <- function(z_hats, K_vec, x_mat) {
  n <- length(z_hats[[1]])
  n_mc <- length(z_hats)
  dummy_blocks <- vector("list", n_mc)
  for (j in seq_len(n_mc)) {
    sj <- K_vec[j] - 1L
    dummies <- matrix(0, n, sj)
    for (k in seq_len(sj)) {
      dummies[, k] <- as.numeric(z_hats[[j]] == k)
    }
    dummy_blocks[[j]] <- dummies
  }
  do.call(cbind, c(dummy_blocks, list(x_mat)))
}

#' Generate parameter names for multiple mc variables
#' @keywords internal
.make_multi_param_names <- function(z_levels_list, K_vec, x_mat) {
  dummy_names <- character(0)
  var_names <- names(z_levels_list)
  for (j in seq_along(z_levels_list)) {
    levs <- z_levels_list[[j]]
    prefix <- var_names[j]
    if (K_vec[j] == 2L) {
      dummy_names <- c(dummy_names, paste0(prefix, levs[2]))
    } else {
      dummy_names <- c(dummy_names, paste0(prefix, levs[-1]))
    }
  }
  c(dummy_names, colnames(x_mat))
}


